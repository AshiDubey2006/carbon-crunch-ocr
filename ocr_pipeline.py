def ocr_to_lines(ocr_result, y_threshold=15):
    ocr_result = sorted(ocr_result, key=lambda x: x[0][0][1])

    lines = []
    current_line = []
    current_y = None

    for bbox, text, conf in ocr_result:
        y = bbox[0][1]

        if current_y is None:
            current_y = y

        if abs(y - current_y) < y_threshold:
            current_line.append((text, conf))
        else:
            line_text = " ".join([t for t, _ in current_line])
            avg_conf = sum([c for _, c in current_line]) / len(current_line)
            lines.append((line_text, avg_conf))

            current_line = [(text, conf)]
            current_y = y

    if current_line:
        line_text = " ".join([t for t, _ in current_line])
        avg_conf = sum([c for _, c in current_line]) / len(current_line)
        lines.append((line_text, avg_conf))

    return lines

