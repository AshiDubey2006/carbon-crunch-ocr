import numpy as np

def correct_skew(image):
    coords = np.column_stack(np.where(image > 0))
    if len(coords) < 10:
        return image
    angle = cv2.minAreaRect(coords)[-1]
    if angle < -45:
        angle = -(90 + angle)
    else:
        angle = -angle
    (h, w) = image.shape[:2]
    M = cv2.getRotationMatrix2D((w//2, h//2), angle, 1.0)
    return cv2.warpAffine(image, M, (w, h), flags=cv2.INTER_CUBIC, borderMode=cv2.BORDER_REPLICATE)

def fix_contrast(image):
    clahe = cv2.createCLAHE(clipLimit=2.0, tileGridSize=(8,8))
    return clahe.apply(image)

def preprocess_image(img_bgr):
    gray = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2GRAY)
    contrast = fix_contrast(gray)
    blur = cv2.GaussianBlur(contrast, (3,3), 0)
    thresh = cv2.threshold(blur, 0, 255, cv2.THRESH_BINARY + cv2.THRESH_OTSU)[1]
    deskewed = correct_skew(thresh)
    return deskewed

def avg_confidence(results):
  if len(results)==0:
    return 0
  return sum([conf for _, _, conf in results])/len(results)

def impword_score(results):
  important_words = ["total", "amount", "date"]
  text_list = [text.lower() for _, text, _ in results]
  score = 0
  for word in important_words:
    if any(word in t for t in text_list):
      score += 1
  return score

def best_ocr(image_bgr):
  results_dict = {}
  results_dict['raw'] = reader.readtext(image_bgr)

  image_gray = cv2.cvtColor(image_bgr, cv2.COLOR_BGR2GRAY)
  results_dict['gray'] = reader.readtext(image_gray)

  image_blur = cv2.GaussianBlur(image_gray, (3,3), 0)
  results_dict['blur'] = reader.readtext(image_blur)

  image_thresh = cv2.threshold(image_blur, 0, 255, cv2.THRESH_BINARY + cv2.THRESH_OTSU)[1]
  results_dict['thresh'] = reader.readtext(image_thresh)

  image_preprocessed = preprocess_image(image_bgr)  # skew + contrast
  results_dict['preprocessed'] = reader.readtext(image_preprocessed)




  best_score = -1
  best_method = None
  best_result = None
  for method, results in results_dict.items():
    avg_conf = avg_confidence(results)
    imp_word_score = impword_score(results)
    total_score = avg_conf + imp_word_score
    print(f"{method}: {total_score:.2f}")
    if total_score > best_score:
      best_score = total_score
      best_method = method
      best_result = results
  return best_method, best_result


