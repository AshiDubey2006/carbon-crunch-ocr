import re
from dateutil import parser as dateparser

def normalize_ocr_text(text):
    replacements = {'0':'O','1':'I','5':'S','8':'B','@':'A','|':'I'}
    result = text.upper()
    for wrong, right in replacements.items():
        result = result.replace(wrong, right)
    return result

def is_total_keyword(text):
    t = normalize_ocr_text(text)
    keywords = ['TOTAL','SUBTOTAL','SUB TOTAL','AMOUNT DUE','AMT DUE',
                'BALANCE DUE','GRAND TOTAL','NET AMOUNT','TOIAI','TOIAL']
    return any(k in t for k in keywords)

def is_skip_line(text):
    skip = ['change','cash','tender','paid','savings','you save',
            'discount','tax','gst','vat','purchase','includes']
    return any(k in text.lower() for k in skip)

def get_price_from_text(text, allow_whole_number=False):
    nums = re.findall(r'\$?\s*(\d{1,6}[.,]\d{2})\b', text)
    if nums:
        return round(float(nums[-1].replace(',','.')), 2)

    if allow_whole_number:
        whole = re.findall(r'\b(\d{2,4})\s*$', text.strip())
        if whole:
            val = float(whole[-1])
            if 1 < val < 10000:
                return round(val, 2)
    return None

def extract_date(lines):
    patterns = [
        r'\b\d{1,2}[\/\-\.]\d{1,2}[\/\-\.]\d{2,4}\b',
        r'\b\d{4}[\/\-\.]\d{1,2}[\/\-\.]\d{1,2}\b',
        r'\b\d{1,2}[\s\-]?(?:Jan|Feb|Mar|Apr|May|Jun|Jul|Aug|Sep|Oct|Nov|Dec)[a-z]*[\s\-]?\d{2,4}\b',
        r'\b(?:Jan|Feb|Mar|Apr|May|Jun|Jul|Aug|Sep|Oct|Nov|Dec)[a-z]*[\s\.\-]\d{1,2}[\s\.\-,]\d{2,4}\b',
    ]
    best_date, best_conf = None, 0.0
    for text, conf in lines:
        for pattern in patterns:
            for match in re.finditer(pattern, text, re.IGNORECASE):
                raw = match.group().strip()

                if re.match(r'^\d{2}:\d{2}(:\d{2})?$', raw):
                    continue

                try:
                    parsed = dateparser.parse(raw, dayfirst=False)
                    if parsed and 1990 <= parsed.year <= 2030:
                        if conf > best_conf:
                            best_date = parsed.strftime('%Y-%m-%d')
                            best_conf = conf
                except:
                    continue
    return best_date, best_conf

def extract_total(lines, raw_ocr_result):
    candidates = []

    for i, (text, conf) in enumerate(lines):
        if is_total_keyword(text) and not is_skip_line(text):
            price = get_price_from_text(text, allow_whole_number=False)
            if price and price > 0:
                score = 3 if 'grand' in text.lower() else 2
                candidates.append((price, score, conf))

    for i, (text, conf) in enumerate(lines[:-1]):
        if is_total_keyword(text) and not is_skip_line(text):
            next_text, next_conf = lines[i+1]
            price = get_price_from_text(next_text, allow_whole_number=False)
            if price and price > 0:
                candidates.append((price, 2, (conf + next_conf) / 2))

    if not candidates:
        sorted_raw = sorted(raw_ocr_result, key=lambda x: x[0][0][1])
        for i, (bbox, text, conf) in enumerate(sorted_raw):
            if is_total_keyword(text) and not is_skip_line(text):
                y_center = (bbox[0][1] + bbox[2][1]) / 2
                for j, (bbox2, text2, conf2) in enumerate(sorted_raw):
                    if i == j:
                        continue
                    y2 = (bbox2[0][1] + bbox2[2][1]) / 2
                    if abs(y_center - y2) < 25:
                        price = get_price_from_text(text2, allow_whole_number=False)
                        if price and price > 0:
                            candidates.append((price, 2, (conf + conf2) / 2))

    if not candidates:
        for i, (text, conf) in enumerate(lines):
            if is_total_keyword(text) and not is_skip_line(text):
                price = get_price_from_text(text, allow_whole_number=True)
                if price and price > 0:
                    candidates.append((price, 1, conf))

    if not candidates:
        return None, 0.0

    candidates.sort(key=lambda x: (x[1], x[0]), reverse=True)
    best_val, _, best_conf = candidates[0]
    return str(round(best_val, 2)), round(best_conf, 2)

def extract_store(lines):
    skip_words = ['gst','tax','invoice','bill','date','receipt','tel','phone',
                  'www','http','total','amount','thank','welcome','items','open',
                  'manager','always','low price','supercenter','hours','sdn','bhd']
    for text, conf in lines[:10]:
        if conf < 0.45:
            continue
        t = text.strip()
        if len(t) < 3:
            continue
        if any(w in t.lower() for w in skip_words):
            continue
        alpha_ratio = sum(c.isalpha() for c in t) / max(len(t), 1)
        if alpha_ratio < 0.4:
            continue
        ascii_ratio = sum(c.isascii() for c in t) / max(len(t), 1)
        if ascii_ratio < 0.6:
            continue
        return t, conf
    return None, 0.0

def clean_item_name(text):
    name = re.sub(r'\b\d{5,}\b', '', text)
    name = re.sub(r'\$?\s*\d+[.,]\d{2}', '', name)
    name = re.sub(r'\b\d+\s*[xX]\s*\d+\b', '', name)
    name = re.sub(r'\s+[FNTXB]\s*$', '', name)
    name = re.sub(r'\b\d+\b', '', name)
    name = re.sub(r'[^\w\s\-&\'/]', ' ', name)
    name = re.sub(r'\s{2,}', ' ', name).strip()
    return name

def extract_items(lines, total_str, raw_ocr_result):
    total_value = float(total_str) if total_str else None
    SKIP_KEYWORDS = ['total','subtotal','sub-total','tax','gst','vat','change',
                     'cash','discount','amount','balance','tender','credit',
                     'debit','visa','mastercard','savings','thank','welcome',
                     'items sold','purchase','qty item','rounded','includes']
    items = []
    seen_prices = set()

    for text, conf in lines:
        if conf < 0.3:
            continue
        t_lower = text.lower()
        if any(kw in t_lower for kw in SKIP_KEYWORDS):
            continue
        if not re.search(r'[A-Za-z]{2,}', text):
            continue

        nums = re.findall(r'\$?\s*(\d{1,4}[.,]\d{2})\b', text)
        if not nums:
            continue

        price_str = nums[-1].replace(',','.')
        price_val = round(float(price_str), 2)

        if price_val <= 0:
            continue
        if total_value and abs(price_val - total_value) < 0.02:
            continue
        if price_val in seen_prices:
            continue
        seen_prices.add(price_val)

        name = clean_item_name(text)
        if len(name) < 3:
            continue

        items.append({
            "name":       name,
            "price":      str(price_val),
            "confidence": round(conf, 2)
        })

    return items

